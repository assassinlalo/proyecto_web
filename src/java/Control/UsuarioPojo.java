package Control;

/**
 *
 * @author lalo
 */
public class UsuarioPojo {
    public Integer uid;
    public String unombre;
    public String ucontrasenia;

    /**
     * @return the uid
     */
    public Integer getUid() {
        return uid;
    }

    /**
     * @param uid the uid to set
     */
    public void setUid(Integer uid) {
        this.uid = uid;
    }

    /**
     * @return the unombre
     */
    public String getUnombre() {
        return unombre;
    }

    /**
     * @param unombre the unombre to set
     */
    public void setUnombre(String unombre) {
        this.unombre = unombre;
    }

    /**
     * @return the ucontrasenia
     */
    public String getUcontrasenia() {
        return ucontrasenia;
    }

    /**
     * @param ucontrasenia the ucontrasenia to set
     */
    public void setUcontrasenia(String ucontrasenia) {
        this.ucontrasenia = ucontrasenia;
    }
}
